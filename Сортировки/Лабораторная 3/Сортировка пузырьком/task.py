from typing import Sequence


def sort(container: Sequence[int]) -> Sequence[int]:
    """
    Сортировка пузырьком

    1. Пройти по всему массиву, сравнивая каждые два соседних элемента.
    2. Если элементы не находятся в нужном порядке, меняйте их местами.
    3. Повторяйте шаг 2, пока не пройдете весь массив без изменений.
    4. Повторяйте шаги 1-3, пока не отсортируете весь массив.

    :param container: Массив, который надо отсортировать
    :return: Отсортированный в порядке возрастания массив
    """
    # TODO реализовать алгоритм сортировки пузырьком
    arr = list(container)  # Создаем копию массива, чтобы не изменять оригинал
    n = len(arr)

    for i in range(n):
        swapped = False  # Флаг для отслеживания перестановок

        # Проходим по массиву до последнего неотсортированного элемента
        for j in range(1, n - i):
            if arr[j - 1] > arr[j]:
                # Меняем элементы местами, если они не в порядке
                arr[j - 1], arr[j] = arr[j], arr[j - 1]
                swapped = True

        # Если не было перестановок, массив отсортирован
        if not swapped:
            break

    return arr


# Примеры использования
if __name__ == '__main__':
    example = [64, 34, 25, 12, 22, 11, 90]
    print(sort(example))  # Ожидаемый результат: [11, 12, 22, 25, 34, 64, 90]